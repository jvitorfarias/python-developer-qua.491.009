# lista
nomes = ["Fulano", "Cicrano", "Beltrano", "João", "Maria", "José"]

# imprime a lista
print(nomes)

# imprime primeiro item
print(nomes[0])

# imprime quarto item
print(nomes[3])

# quantos itens tem a lista
print(len(nomes))

# imprime um item embaixo do outro
for nome in nomes:
    print(nome)

# ordem alfabética
nomes.sort()
